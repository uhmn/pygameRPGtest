def div(lis, num):
    return (lis[0]/num,lis[1]/num)
    
def mult(lis, num):
    return (lis[0]*num,lis[1]*num)
    
def add(lis, num):
    return (lis[0]+num,lis[1]+num)
    
def sub(lis, num):
    return (lis[0]-num,lis[1]-num)

def round2(lis):
    return (round(lis[0]),round(lis[1]))



